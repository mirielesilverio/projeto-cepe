    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        DefaultTableModel modelo = (DefaultTableModel)jtChamadas.getModel();
        presencaAc = new PresencaAccess();
        presenca = new Presenca();
        alunoAc = new AlunoAccess();
        for(int i=0;i<modelo.getRowCount();i++)
        {
            presenca.setIdAluno(alunoAc.pegaId((String) modelo.getValueAt(i,0)));
            
            presenca.setIdChamada(chamada.getId());
            
            if((Boolean)modelo.getValueAt(i, 2)==true)
                presenca.setSituacao(1);
            else
                presenca.setSituacao(0);
            
            presencaAc.create(presenca);
        }
        
    }//GEN-LAST:event_btnSalvarActionPerformed

