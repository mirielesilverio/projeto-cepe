    private void btnChamadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChamadaActionPerformed
        PreencheTabela preenche = new PreencheTabela(jtChamadas,jcbTurmas.getSelectedIndex()+1);
        
        jpnEspera.setVisible(false);
        jpnChamada.setVisible(true);
        btnSalvar.setVisible(true);
        
        chamada = new Chamada();
        chamada.setData(jftDataAula.getText());
        chamada.setIdClasse(jcbTurmas.getSelectedIndex()+1);
        chamada.setIdDisciplina(professor.getMateria());              
        chamada.setIdProfessor(professor.getIdProfessor());
        chamada.setHorario(jftHorarioAUla.getText());
        
        chamadaAc = new ChamadaAccess();
        chamadaAc.create(chamada);
        chamada.setId(chamadaAc.pegaId(chamada.getHorario(), professor.getIdProfessor(),chamada.getData()));
        JOptionPane.showMessageDialog(null,chamada.getId());
        
    }//GEN-LAST:event_btnChamadaActionPerformed

